// StrippedThumbExpander.cs — Vianigram.App.Services
//
// Telegram's `userProfilePhoto` / `chatPhoto` constructors carry an
// inline thumbnail called the "stripped photo": ~80 bytes of JPEG
// data with the standard SOI/quantization/Huffman tables stripped out
// to save wire space. Reconstruction is done by:
//
//   1) Prepending a fixed JPEG header (the missing SOI / Quantization
//      tables / SOF marker / Huffman tables / SOS marker — "JPG_HEADER"
//      below).
//   2) Patching two width/height bytes inside the header from bytes
//      [1] and [2] of the stripped payload.
//   3) Appending the body of the stripped payload (bytes [3..]).
//   4) Appending the JPEG end-of-image marker (0xFF, 0xD9).
//
// The exact constants are from TDLib's mtproto/files/PhotoSizeStripped
// implementation (public domain). They produce a tiny (typically
// 20-50 px on the long side) but recognisable thumbnail — enough to
// fill the avatar circle without a network round-trip.
//
// This expander runs on a worker thread (no UI blocking); the
// resulting JPEG bytes are fed into `BitmapImage.SetSourceAsync` on
// the UI thread by the caller.

