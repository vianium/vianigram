        private static readonly byte[] JpgFooter = new byte[] { 0xff, 0xd9 };

        // SOF0 (Start Of Frame, baseline DCT) layout inside the header:
        //   pos 159: 0xFF
        //   pos 160: 0xC0 (SOF0 marker)
        //   pos 161-162: segment length (0x00 0x11 = 17)
        //   pos 163: precision (0x08 = 8 bits)
        //   pos 164-165: image height (big-endian uint16)
        //   pos 166-167: image width (big-endian uint16)
        //   pos 168: number of components (0x03 = YCbCr)
        //
        // Stripped thumbnails encode width/height as single bytes (since
        // they're tiny — typically ≤ 256 px). We write 0 to the high
        // bytes (164/166) and the value to the low bytes (165/167) so
        // the SOF declares dimensions that match the encoded data.
        //
        // Note: TDLib / Telethon / Pyrogram all patch only positions
        // 164/166 (high bytes), which technically declares dimensions
        // 256×stripped[1] in the JPEG header. Most forgiving decoders
        // ignore SOF dimensions and re-derive from MCU placement, so it
        // "works" elsewhere. WinRT's BitmapImage on WP 8.1 is stricter,
        // so we set canonical low-byte dimensions to keep the decoder
        // happy and avoid a 7680×5120 declaration that gets rejected.
        private const int HeightHighIndex = 164;
        private const int HeightLowIndex  = 165;
        private const int WidthHighIndex  = 166;
        private const int WidthLowIndex   = 167;

        /// <summary>
        /// Reconstruct a full JPEG byte sequence from a Telegram
        /// "stripped" thumb. Returns null when the input is not a valid
        /// stripped thumb (must start with 0x01 and be at least 3 bytes).
        /// Pure CPU work — safe on any thread.
        /// </summary>
        public static byte[] Expand(byte[] stripped)
        {
            if (stripped == null || stripped.Length < 3) return null;
            if (stripped[0] != 0x01) return null;

            // Telegram convention (per TDLib's get_dimensions(bytes[1], bytes[2])):
            //   stripped[1] = width  (single byte, since thumbs are ≤ 256 px)
            //   stripped[2] = height (single byte)
            byte width = stripped[1];
            byte height = stripped[2];
            int bodyLen = stripped.Length - 3;

            byte[] result = new byte[JpgHeader.Length + bodyLen + JpgFooter.Length];
            System.Buffer.BlockCopy(JpgHeader, 0, result, 0, JpgHeader.Length);
            // Big-endian uint16 — high byte stays 0; low byte gets the
            // actual dimension. Both bytes are explicitly written so the
            // expander is robust against any future tweak to the header
            // constants.
            result[HeightHighIndex] = 0;
            result[HeightLowIndex] = height;
            result[WidthHighIndex] = 0;
            result[WidthLowIndex] = width;
            System.Buffer.BlockCopy(stripped, 3, result, JpgHeader.Length, bodyLen);
            System.Buffer.BlockCopy(JpgFooter, 0, result, JpgHeader.Length + bodyLen, JpgFooter.Length);
            return result;
        }

        /// <summary>
        /// Async helper: reconstruct + materialise as a
        /// <see cref="BitmapImage"/> ready to bind to an Image control.
        /// MUST be awaited on the UI thread (BitmapImage construction
        /// has UI affinity on WP 8.1). Returns null on malformed input.
        /// </summary>
        public static async Task<BitmapImage> ExpandToBitmapAsync(byte[] stripped)
        {
            byte[] jpeg = Expand(stripped);
            if (jpeg == null) return null;

            using (var ms = new InMemoryRandomAccessStream())
            {
                using (var writer = new DataWriter(ms))
                {
                    writer.WriteBytes(jpeg);
                    await writer.StoreAsync();
                    await writer.FlushAsync();
                    writer.DetachStream();
                }
                ms.Seek(0);
                var bmp = new BitmapImage();
                try
                {
                    await bmp.SetSourceAsync(ms);
                }
                catch
                {
                    // Malformed payload — fall back to initials.
                    return null;
                }
                return bmp;
            }
        }
